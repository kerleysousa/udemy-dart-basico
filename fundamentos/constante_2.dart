main () {

  // Esta é uma lista em que o tipo final é determinado
  final lista1 = ["Ana", "Beatriz", "Carla"];
  lista1.add("Denise");
  print(lista1);

  // Esta é uma lista em que o tipo final é determinado e os elementos são constantes
  final lista2 = const ["Maçã", "Banana", "Melancia"];
  print(lista2);

}