main () {

// Operadores Aritméticos
  var a = 10;
  var b = 5;

  var resultado = a + b;

  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);
  
// Operadores Lógicos

  bool flores = true;
  bool chocolate = false;

  print(flores && chocolate); // E
  print(flores || chocolate); // OU
  print(flores ^ chocolate); // OU Exclusivo
  print(!flores); // Negação
  print(!chocolate); // Negação

}